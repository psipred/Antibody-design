"""
abb2_inference.py
=================
Batch-runs ABodyBuilder2 (ImmuneBuilder) 3-D structure prediction on a
directory of per-antibody FASTA files and saves one PDB per antibody.

Overview
--------
ABodyBuilder2 is a deep-learning model for high-accuracy prediction of
antibody Fv (VH+VL) structures, with particular strength on CDR loops.
This script wraps the ABodyBuilder2 command-line tool to process an entire
directory of FASTA files in sequence, collecting success and failure counts.

Each input FASTA file must have exactly two sequences in ImmuneBuilder format:
    >H
    <heavy-chain sequence>
    >L
    <light-chain sequence>

This format is produced upstream by fasta_converter_immunebuilder.py.

Inputs
------
  INPUT_DIR : directory of .fasta files, one per antibody pair
              (produced by fasta_converter_immunebuilder.py)

Outputs
-------
  OUTPUT_DIR/<name>.pdb for each successfully predicted structure.
  Failed entries are printed at the end but not re-attempted.

"""

import os
import subprocess

# ── SETTINGS ──────────────────────────────────────────────────────────────────
# INPUT_DIR should be a folder of per-antibody .fasta files generated by
# fasta_converter_immunebuilder.py. Each file must follow ImmuneBuilder's
# >H / >L header convention.
INPUT_DIR   = "/home/alanwu/Documents/iggen_model/model_output/fasta/1A/abb2_pairs_split.fasta"
OUTPUT_DIR  = "/home/alanwu/Documents/iggen_model/immunebuilder_output/1A"
# Chothia numbering is required for consistency with the ANARCI files used
# in the downstream confidence-score and pLDDT evaluation scripts.
NUMBERING   = "chothia"
# ──────────────────────────────────────────────────────────────────────────────

os.makedirs(OUTPUT_DIR, exist_ok=True)

fasta_files = [f for f in os.listdir(INPUT_DIR) if f.endswith(".fasta")]

if not fasta_files:
    print(f"[ERROR] No .fasta files found in {INPUT_DIR}")
    exit(1)

print(f"Found {len(fasta_files)} fasta files to process\n")

success = 0
failed  = []

for fasta_file in fasta_files:
    name      = fasta_file.replace(".fasta", "")
    input_path  = os.path.join(INPUT_DIR, fasta_file)
    output_path = os.path.join(OUTPUT_DIR, f"{name}.pdb")

    print(f"Predicting {name}...")

    cmd = [
        "ABodyBuilder2",
        "-f", input_path,
        "-o", output_path,
        "-n", NUMBERING,
        # -v enables verbose output from ABodyBuilder2, useful for tracking
        # progress and identifying per-structure failures in a long batch.
        "-v"
    ]

    # capture_output=False lets ABodyBuilder2 print directly to the terminal
    # so progress (including GPU memory usage and per-model scores) is visible.
    result = subprocess.run(cmd, capture_output=False, text=True)

    if result.returncode == 0:
        print(f"  [OK] Saved {output_path}\n")
        success += 1
    else:
        # Non-zero exit is treated as a soft failure: the name is recorded and
        # the batch continues rather than crashing mid-run.
        print(f"  [FAILED] {name}\n")
        failed.append(name)

print(f"\nDone! {success}/{len(fasta_files)} structures predicted successfully.")
if failed:
    print(f"Failed: {', '.join(failed)}")
