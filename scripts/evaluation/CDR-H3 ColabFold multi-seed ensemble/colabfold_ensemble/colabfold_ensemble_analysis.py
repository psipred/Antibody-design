"""
colabfold_ensemble_analysis.py
==============================
Analyses CDR-H3 structural determinism by comparing mean pairwise RMSD
distributions between two antibody sequence sets:

  - **Baseline (IgGen)**  – sequences generated by the baseline (pre-fine-tuning) model.
  - **Finetuned (OAS/SAbDab)** – sequences generated after fine-tuning on OAS/SAbDab data.

Each CSV was produced upstream by running ColabFold with multiple random seeds
(see colabfold_ensemble.py) and then clustering the resulting CDR-H3 Cα traces
with complete-linkage clustering at a 1.50 Å cutoff.  The clustering step
converts a set of individual RMSD values into a single per-sequence summary
statistic: the *mean pairwise Cα RMSD* across all seed predictions.  A low
value means the model's CDR-H3 predictions are tightly self-consistent
(structurally deterministic); a high value means they scatter across
conformational space.

**Inputs**
  Two CSV files, each containing at minimum a column named
  ``mean_pairwise_h3_rmsd_CA_A`` (one row per sequence).

**Outputs**
  1. ``h3_rmsd_iggen_vs_oas_violin.png``  – violin plot of RMSD distributions,
     annotated with per-group medians and a Mann-Whitney U significance bracket.
  2. ``h3_rmsd_percentage_bar.png``       – bar chart showing the percentage of
     sequences whose mean pairwise RMSD falls below 1.50 Å (i.e. effectively
     only one structural cluster).

**Key design choices**
  - Mann-Whitney U (non-parametric) is used instead of a t-test because RMSD
    distributions are right-skewed and there is no reason to assume normality.
  - The 1.50 Å threshold mirrors the complete-linkage clustering cutoff used
    upstream, so "below threshold" ≡ "all seeds landed in a single cluster".
  - Medians (not means) are annotated on the violin because RMSD distributions
    are skewed; the median is more representative of central tendency.

**How to run**
  python colabfold_ensemble_analysis.py

  Adjust the IGGEN_CSV / OAS_CSV / OUT_DIR paths in the CONFIG section as
  needed before running.
"""

import os
import numpy as np
import pandas as pd
import matplotlib.pyplot as plt
from scipy.stats import mannwhitneyu

plt.rcParams.update({'font.size': 14})

# =========================
# CONFIG
# =========================
IGGEN_CSV = "/home/alanwu/Documents/iggen_model/evaluation_metrics/multiseed_alphafold/iggen/h3_mean_pairwise_rmsd_CA_complete_link_1.50A.csv"
OAS_CSV   = "/home/alanwu/Documents/iggen_model/evaluation_metrics/multiseed_alphafold/oas_v5/h3_mean_pairwise_rmsd_CA_complete_link_1.50A.csv"

OUT_DIR = "/home/alanwu/Documents/iggen_model/evaluation_metrics/multiseed_alphafold"
os.makedirs(OUT_DIR, exist_ok=True)

OUT_VIOLIN = os.path.join(OUT_DIR, "h3_rmsd_iggen_vs_oas_violin.png")
OUT_BAR    = os.path.join(OUT_DIR, "h3_rmsd_percentage_bar.png")

RMSD_COL = "mean_pairwise_h3_rmsd_CA_A"
THRESHOLD = 1.5  # Å — matches the complete-linkage clustering cutoff used upstream


def pvalue_to_sig_label(pvalue):
    # Standard three-star significance convention used in structural biology
    if pvalue < 0.001:
        return "***"
    if pvalue < 0.01:
        return "**"
    if pvalue < 0.05:
        return "*"
    return "ns"


def load_rmsd_values(csv_path):
    df = pd.read_csv(csv_path)

    if RMSD_COL not in df.columns:
        raise RuntimeError(f"Column '{RMSD_COL}' not found in {csv_path}")

    vals = pd.to_numeric(df[RMSD_COL], errors="coerce")
    # Drop NaN/inf that arise from sequences where clustering produced no valid RMSD
    vals = vals[np.isfinite(vals)].to_numpy(dtype=float)

    if len(vals) == 0:
        raise RuntimeError(f"No valid RMSD values found in {csv_path}")

    return vals


def plot_violin(iggen_vals, oas_vals, outpath):
    fig, ax = plt.subplots(figsize=(5, 6))

    data = [iggen_vals, oas_vals]
    labels = ["Baseline", "Finetuned"]
    colors = ["#f5c87a", "#a8c8e8"]

    # showmedians/showextrema are disabled because we draw custom median text
    # annotations instead, which are easier to read at publication scale
    parts = ax.violinplot(
        data,
        positions=[1, 2],
        widths=0.6,
        showmedians=False,
        showextrema=False
    )

    for pc, color in zip(parts["bodies"], colors):
        pc.set_facecolor(color)
        pc.set_edgecolor("none")
        pc.set_alpha(0.85)

    # Median annotation (3 significant figures)
    for vals, pos in zip(data, [1, 2]):
        median = np.median(vals)
        ax.text(
            pos,
            median + 0.02,
            "{:.3g} Å".format(median),
            ha="center",
            va="bottom",
            fontsize=14
        )

    # Mann-Whitney U test on mean pairwise RMSD distributions.
    # Two-sided because we have no directional prior at the time of plotting.
    _, pvalue = mannwhitneyu(iggen_vals, oas_vals, alternative="two-sided")
    sig_label = pvalue_to_sig_label(pvalue)

    # Draw a significance bracket above the tallest violin using four line
    # segments: two vertical ticks and one horizontal bar
    y_data_max = max(np.max(iggen_vals), np.max(oas_vals))
    bracket_y = y_data_max + 0.08
    bracket_h = 0.04
    text_y = bracket_y + bracket_h + 0.01
    ax.plot([1, 1, 2, 2], [bracket_y, bracket_y + bracket_h, bracket_y + bracket_h, bracket_y], c="black", lw=1.2)
    ax.text(1.5, text_y, sig_label, ha="center", va="bottom", fontsize=14)
    print("Mann-Whitney U (Baseline vs Finetuned): p={:.6g} ({})".format(pvalue, sig_label))

    ax.set_xticks([1, 2])
    ax.set_xticklabels(labels, fontsize=14)
    ax.set_ylabel("Mean pairwise CDR H3 RMSD [Å]", fontsize=14)

    ax.spines["top"].set_visible(False)
    ax.spines["right"].set_visible(False)

    ax.set_xlim(0.3, 2.7)
    ax.set_ylim(bottom=0)

    plt.tight_layout()
    plt.savefig(outpath, dpi=150, bbox_inches="tight")
    plt.close()

    print("Saved:", outpath)


def compute_percentage_below(vals, threshold):
    return 100.0 * np.sum(vals < threshold) / len(vals)


def plot_percentage_bar(iggen_vals, oas_vals, outpath):
    fig, ax = plt.subplots(figsize=(5, 6))

    labels = ["Baseline", "Finetuned"]
    colors = ["#f5c87a", "#a8c8e8"]

    iggen_pct = compute_percentage_below(iggen_vals, THRESHOLD)
    oas_pct   = compute_percentage_below(oas_vals, THRESHOLD)

    values = [iggen_pct, oas_pct]

    bars = ax.bar([1, 2], values, color=colors, width=0.6)

    # Annotate each bar with its exact percentage value
    for bar, val in zip(bars, values):
        ax.text(
            bar.get_x() + bar.get_width() / 2,
            val + 1,
            "{:.3g}%".format(val),
            ha="center",
            va="bottom",
            fontsize=14
        )

    ax.set_xticks([1, 2])
    ax.set_xticklabels(labels, fontsize=14)
    # "Only one cluster" here means all 8 seeds converged tightly enough that
    # complete-linkage clustering at 1.50 Å groups them into a single cluster
    ax.set_ylabel(f"% with only one H3 cluster (<{THRESHOLD:.3g} Å)", fontsize=14)

    ax.spines["top"].set_visible(False)
    ax.spines["right"].set_visible(False)

    ax.set_xlim(0.3, 2.7)
    ax.set_ylim(0, 100)

    plt.tight_layout()
    plt.savefig(outpath, dpi=150, bbox_inches="tight")
    plt.close()

    print("Saved:", outpath)


# =========================
# RUN
# =========================
iggen_vals = load_rmsd_values(IGGEN_CSV)
oas_vals   = load_rmsd_values(OAS_CSV)

print("IgGen:      n={} median={:.3g} Å mean={:.3g} Å".format(
    len(iggen_vals), np.median(iggen_vals), np.mean(iggen_vals)
))
print("SAbDab+OAS: n={} median={:.3g} Å mean={:.3g} Å".format(
    len(oas_vals), np.median(oas_vals), np.mean(oas_vals)
))

print("IgGen:      {:.3g}% below {:.3g} Å".format(
    compute_percentage_below(iggen_vals, THRESHOLD), THRESHOLD
))
print("SAbDab+OAS: {:.3g}% below {:.3g} Å".format(
    compute_percentage_below(oas_vals, THRESHOLD), THRESHOLD
))

plot_violin(iggen_vals, oas_vals, OUT_VIOLIN)
plot_percentage_bar(iggen_vals, oas_vals, OUT_BAR)
